package cn.adwalker.ad.admin.channel.bean;

public class ChannelRatingBean {
	private Long channelID;
	private String channelName;
	private String channelRating;
	private String os;
	public Long getChannelID() {
		return channelID;
	}
	public void setChannelID(Long channelID) {
		this.channelID = channelID;
	}
	public String getChannelName() {
		return channelName;
	}
	public void setChannelName(String channelName) {
		this.channelName = channelName;
	}
	public String getChannelRating() {
		return channelRating;
	}
	public void setChannelRating(String channelRating) {
		this.channelRating = channelRating;
	}
	public String getOs() {
		return os;
	}
	public void setOs(String os) {
		this.os = os;
	}
	
	
}
