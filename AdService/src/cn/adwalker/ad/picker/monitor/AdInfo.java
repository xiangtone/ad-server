package cn.adwalker.ad.picker.monitor;

public class AdInfo {
 
	private Integer adId;
	private Integer cpm=0;
	private Integer cpc=0;
	private Integer cpa=0;
	private Integer cpd=0;
	
	
	public Integer getCpd() {
		return cpd;
	}
	public void setCpd(Integer cpd) {
		this.cpd = cpd;
	}
	public Integer getAdId() {
		return adId;
	}
	public void setAdId(Integer adId) {
		this.adId = adId;
	}
	public Integer getCpm() {
		return cpm;
	}
	public void setCpm(Integer cpm) {
		this.cpm = cpm;
	}
	public Integer getCpc() {
		return cpc;
	}
	public void setCpc(Integer cpc) {
		this.cpc = cpc;
	}
	public Integer getCpa() {
		return cpa;
	}
	public void setCpa(Integer cpa) {
		this.cpa = cpa;
	}
	
	
}
